char	*ft_strupcase(char *str)
{
	char	temp;
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			temp = str[i] - 32;
			str[i] = temp;
		}
		i++;
	}
	return (str);
}
